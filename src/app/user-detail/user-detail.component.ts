  ngOnInit(): void {
    let id = this.route.snapshot.paramMap.get('id');
    this.loadBlankUser();
    this.workContactInfo = {} as ContactInfo;
    this.homeContactInfo = {} as ContactInfo;
    if (id && id !== 'add') {
      this.loadUser(id);
    }
    this.loadCountryList();
  }

  async loadUser(id: string) {
    try {
      this.user = await this.userService.getUser(id);
      this.workContactInfo = this.getContactInfo('work');
      this.homeContactInfo = this.getContactInfo('home');
    }
    catch (e) {
      console.error(e);
      this.snackbar.open('User data failed to load', undefined, {
        duration: 3000
      });
    }

  }

  private getContactInfo(type: string) {
    let info = {} as ContactInfo;
    let addrMatch = this.user.addresses.find((a) => a.type === type);
    if (addrMatch) {
      info.country = addrMatch.country;
      info.streetAddress = addrMatch.streetAddress;
      info.poBox = addrMatch.poBox;
      info.locality = addrMatch.locality;
      info.region = addrMatch.region;
      info.postalCode = addrMatch.postalCode;
    }
    let phoneMatch = this.user.phoneNumbers.find((a) => a.type === type);
    if (phoneMatch) {
      info.phone = phoneMatch.number;
    }
    let cellMatch = this.user.phoneNumbers.find((a) => a.type === type + "_mobile");
    if (cellMatch) {
      info.cell = cellMatch.number;
    }
    let faxMatch = this.user.phoneNumbers.find((a) => a.type === type + "_fax");
    if (faxMatch) {
      info.fax = faxMatch.number;
    }
    return info;
  }

  private setContactInfo(info: ContactInfo, type: 'work' | 'home') {
    let addrMatch = this.user.addresses.find((a) => a.type === type);
    if (info.country || info.streetAddress || addrMatch) {
      if (!addrMatch) {
        addrMatch = {
          type: type,
          streetAddress: info.streetAddress,
          country: info.country,
          locality: info.locality,
          region: info.region,
          poBox: info.poBox,
          postalCode: info.postalCode
        };
        this.user.addresses.push(addrMatch)
      }
      else {
        addrMatch.streetAddress = info.streetAddress;
        addrMatch.country = info.country;
        addrMatch.poBox = info.poBox;
        addrMatch.locality = info.locality;
        addrMatch.region = info.region;
        addrMatch.postalCode = info.postalCode
      }
    }
    let phoneMatch = this.user.phoneNumbers.find((p) => p.type === type);
    if (info.phone || phoneMatch) {
      if (!phoneMatch) {
        phoneMatch = {
          type: type,
          number: info.phone
        }
        this.user.phoneNumbers.push(phoneMatch)
      }
      else {
        phoneMatch.number = info.phone;
      }
    }
    let cellMatch = this.user.phoneNumbers.find((p) => p.type === type + "_mobile");
    if (info.cell || cellMatch) {
      if (!cellMatch) {
        cellMatch = {
          type: type,
          number: info.cell
        }
        this.user.phoneNumbers.push(cellMatch)
      }
      else {
        cellMatch.number = info.cell;
      }
    }
    let faxMatch = this.user.phoneNumbers.find((p) => p.type === type + "_fax");
    if (info.fax || faxMatch) {
      if (!faxMatch) {
        faxMatch = {
          type: type,
          number: info.fax
        }
        this.user.phoneNumbers.push(faxMatch)
      }
      else {
        faxMatch.number = info.fax;
      }
    }
  }

  close() {
    this.router.navigate(['..']);
  }

  async save() {
    this.setContactInfo(this.workContactInfo, 'work');
    this.setContactInfo(this.homeContactInfo, 'home');
    try {
      if (this.user.id) {
        await this.userService.updateUser(this.user);
      }
      else {
        await this.userService.addUser(this.user);
      }
      this.close();
    }
    catch (e) {
      console.log(e);
      this.snackbar.open('User data failed to save', undefined, {
        duration: 3000
      });
    }
  }

